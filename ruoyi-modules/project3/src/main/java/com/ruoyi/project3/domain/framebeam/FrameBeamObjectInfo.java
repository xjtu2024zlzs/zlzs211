package com.ruoyi.project3.domain.framebeam;

public class FrameBeamObjectInfo
{
    private String aircraftModel;
    private String fuselageArea;
    private String frameJointArea;
    private String measurePoint;

    public String getAircraftModel()
    {
        return aircraftModel;
    }

    public void setAircraftModel(String aircraftModel)
    {
        this.aircraftModel = aircraftModel;
    }

    public String getFuselageArea()
    {
        return fuselageArea;
    }

    public void setFuselageArea(String fuselageArea)
    {
        this.fuselageArea = fuselageArea;
    }

    public String getFrameJointArea()
    {
        return frameJointArea;
    }

    public void setFrameJointArea(String frameJointArea)
    {
        this.frameJointArea = frameJointArea;
    }

    public String getMeasurePoint()
    {
        return measurePoint;
    }

    public void setMeasurePoint(String measurePoint)
    {
        this.measurePoint = measurePoint;
    }
}
